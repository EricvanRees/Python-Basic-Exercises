"""
Write a Python program that will accept the base and height of a triangle and compute its area.
"""

def calc_area_of_triangle(base, height):
  return base * height / 2 

print(calc_area_of_triangle(10, 5))
print(calc_area_of_triangle(5, 2))
print(calc_area_of_triangle(6, 1))
print(calc_area_of_triangle(23, 21))